#!/bin/bash

if [ $1 ]
then
    project_name=$1

    echo ""
    echo -e "\e[32m###############################################################################################\e[0m"
    echo -e "\e[32m# CRIANDO DIRETÃ“RIO ###########################################################################\e[0m"
    echo -e "\e[32m###############################################################################################\e[0m"
    echo ""

    mkdir $project_name
    cd $project_name

    echo ""
    echo -e "\e[32m###############################################################################################\e[0m"
    echo -e "\e[32m# CRIANDO VIRTUALENV ##########################################################################\e[0m"
    echo -e "\e[32m###############################################################################################\e[0m"
    echo ""

    virtualenv env -p python3
    . env/bin/activate

    echo ""
    echo -e "\e[32m###############################################################################################\e[0m"
    echo -e "\e[32m# INSTALANDO DJANGO ###########################################################################\e[0m"
    echo -e "\e[32m###############################################################################################\e[0m"
    echo ""

    pip install django

    echo ""
    echo -e "\e[32m###############################################################################################\e[0m"
    echo -e "\e[32m# INICIANDO PROJETO ###########################################################################\e[0m"
    echo -e "\e[32m###############################################################################################\e[0m"
    echo ""

    django-admin startproject --template https://github.com/cosmunsoftwares/django-boilerplate/archive/api.zip $project_name .

if [ $2 ]
then
    find ./ -type f -exec sed -i '' -e s/project_name/$project_name/g {} \;
    rm -f locale/pt_BR/LC_MESSAGES/.*
else
    find ./ -type f -exec sed -i s/project_name/$project_name/g {} \;
fi

    pip install -r requirements_dev.txt

    cp contrib/env-sample .env

    echo ""
    echo -e "\e[32m###############################################################################################\e[0m"
    echo -e "\e[32m# REMOVENDO setup.py ##########################################################################\e[0m"
    echo -e "\e[32m###############################################################################################\e[0m"
    echo ""

    rm -f setup.sh

    echo ""
    echo -e "\e[32m##############################################################################################\e[0m"
    echo -e "\e[32m# VERIFICANDO PROJETO ########################################################################\e[0m"
    echo -e "\e[32m##############################################################################################\e[0m"
    echo ""

    python manage.py test

    echo ""

    python manage.py check

    echo ""
    echo -e "\e[32m##############################################################################################\e[0m"
    echo -e "\e[32m# Finalizado #################################################################################\e[0m"
    echo -e "\e[32m##############################################################################################\e[0m"
    echo ""

else
    echo -e "\e[31m##############################################################################################\e[0m"
    echo -e "\e[31m# INFORME O NOME DO PROJETO ##################################################################\e[0m"
    echo -e "\e[31m##############################################################################################\e[0m"
fi
